/*Various Data Types in Dart !!
  1.Numbers: int, double. no Float or long 
  2.Strings
  3.Booleans
  4.Lists(Arrays)
  5.Maps
  6.Runes(for expressing Unicode Characters in String)
  7.Symbols*/

void main() {
  
    int age=23; //Integer type variable
    print(age);

    var marks=50; //It is inferred as integer automatically
    print(marks);
  
    String name1="Shreyam"; //String type variable
    print(name1);
    
    var name2="Nancy"; //It is inferred as String automatically
    print(name2);
    
    double percentage1=45.22;  //Double type Variable
    print(percentage1);
    
    var percentage2=89.12;   //It is inferred as Double automatically
    print(percentage2);
    
    bool isAlive1=true;  //Boolean type variable
    print(isAlive1);
    
    var isAlive2=false;  //It is inferred as boolean automatically
    print(isAlive2);
}

//All Datatypes in Dart are Objects !!!