void main() { 
  
 //Switch-Case Statements

String grade="D";
  
  switch(grade)
  {
    case 'A' : print("Excellent Grade !");
               break;
    case 'B' : print("Very Good Grade !");
               break;
    case 'C' : print("Average Grade !");
               break;
    case 'F' : print("Failed !");
               break;
    default  : print("Invalid Grade");
      
  }


}